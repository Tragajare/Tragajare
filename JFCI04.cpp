// (= é uma atribuição) (== é igualdade)
// %d serve para dizer onde a variavel deve entrar, somente para numeros INTEIROS
// /n indica quantas linhas vai pular
// /t significa tabulação

#include<stdio.h>
//#include<iostream>

main()
{
int X, S;

X = 122;
S = 9999993;

printf("A variavel N contem:%d\n", X);
printf("A variavel S contem:%d\n\n", S);
printf("%d em N\t  %d em S\n\n", X, S);

}
